    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
                if((null == txtName.getText() || txtName.getText().isEmpty())
                && (null == txtEmployeeId.getText() || txtEmployeeId.getText().isEmpty()))
        {
            JOptionPane.showMessageDialog(this, "Please enter employee Name or Employee ID");
            throw new UnsupportedOperationException("Missing mandatory fields");
        }
        
        String gender = (String)btnGender.getSelectedItem();
        if(((String)btnGender.getSelectedItem()).equalsIgnoreCase("Select")){
            
            gender = null;
        }
        
        String month = (String)lblMonth.getSelectedItem();
        String day = (String)lblDate.getSelectedItem();
        String year = (String)lblYear.getSelectedItem();
      
        if(month.equalsIgnoreCase("Month")){
            
            month = null;
        }
        
        if(day.equalsIgnoreCase("Day")){
            
            day = null;
        }
        
        if(year.equalsIgnoreCase("Year")){
            
            year = null;
        }
        
        String date = null;
        
        if(month!=null && day!=null && year!=null){
            
            date = month + "-" + day + "-" + year;
            
        }
        
      
        Employees employees = Employees.getInstance();
        
        Employee emp = null;
        
        if(!(txtName.getText() == null || txtName.getText().isEmpty()))
        {
            emp = employees.getNameToEmployee().get(txtName.getText());
        }
        
        if(!(txtEmployeeId.getText() == null || txtEmployeeId.getText().isEmpty()))
        {
            String regexEmployeeId = "^\\d{4}$";
            Pattern patternEmployeeId = Pattern.compile(regexEmployeeId);
            Matcher matcherEmployeeId = patternEmployeeId.matcher(txtEmployeeId.getText());
      
            if(!matcherEmployeeId.matches()){
          
                JOptionPane.showMessageDialog(this, "Enter your 4 digit Employee ID");
                  throw new UnsupportedOperationException("Enter your 4 digit Employee ID");
            }
            emp = employees.getEmpIdToEmployee().get(txtEmployeeId.getText());
        }
        
        if(null == emp)
        {
            JOptionPane.showMessageDialog(this, "Please Add Employee to the System");
            throw new UnsupportedOperationException("No Employee exist in the System");
        }
        
        if(!(txtAge.getText() == null || txtAge.getText().isEmpty()))
        {
            if(Integer.parseInt(txtAge.getText())<18){
          
                JOptionPane.showMessageDialog(this, "Employee should be above 18 years");
                  throw new UnsupportedOperationException("Employee should be above 18 years");
            
      }
            emp.setAge(Integer.parseInt(txtAge.getText()));
        }
        
        if(!(gender == null || gender.isEmpty()))
        {
            emp.setGender(gender);
        }
        
        if(!(date == null || date.isEmpty()))
        {
            emp.setStartDate(LocalDate.parse(date, DateTimeFormatter.ofPattern("MM-dd-yyyy")));
        }
        
        if(!(txtLevel.getText() == null || txtLevel.getText().isEmpty()))
        {
            if(Integer.parseInt(txtLevel.getText())<1 || Integer.parseInt(txtLevel.getText())>=10){
          
                JOptionPane.showMessageDialog(this, "Level Should be 1 to 10");
                throw new UnsupportedOperationException("Level Should be 1 to 10");
          
            }
           emp.setLevel(Integer.parseInt(txtLevel.getText()));
        }
        
        if(!(txtTeamInfo.getText() == null || txtTeamInfo.getText().isEmpty()))
        {
           emp.setTeamInfo(txtTeamInfo.getText());
        }
        
        if(!(txtPositionTitle.getText() == null || txtPositionTitle.getText().isEmpty()))
        {
           emp.setPositionTitle(txtPositionTitle.getText());
        }
        
        if(!(txtPhone.getText() == null || txtPhone.getText().isEmpty()))
        {
            
            String regexPhone = "^(\\d{3}[- .]?){2}\\d{4}$";
            Pattern patternPhone = Pattern.compile(regexPhone);
            Matcher matcherPhone = patternPhone.matcher(txtPhone.getText());
      
            if(!matcherPhone.matches()){
          
                JOptionPane.showMessageDialog(this, "Invalid Phone Number");
                  throw new UnsupportedOperationException("Please enter correct Phone number");
            }
           emp.getContactInfo().setCell(txtPhone.getText());
        }
        
        if(!(txtEmail.getText() == null || txtEmail.getText().isEmpty()))
        {
            String regex = "^(.+)@(.+)$";
            Pattern pattern = Pattern.compile(regex);
            Matcher matcher = pattern.matcher(txtEmail.getText());
            
            if(!matcher.matches()){

            JOptionPane.showMessageDialog(this, "Invalid Email id");
            throw new UnsupportedOperationException("Please enter correct Email");
        }
            emp.getContactInfo().setEmail(txtEmail.getText());
    }
        
        if(!(selectedImagePath == null || selectedImagePath.isEmpty())){
            
            emp.setPhoto(selectedImagePath);
        }

        IndexEmployees indexEmployess = new IndexEmployees();
        indexEmployess.indexEmployees(emp);

        JOptionPane.showMessageDialog(this, "Updated Employee Details");
    }//GEN-LAST:event_btnSaveActionPerformed

