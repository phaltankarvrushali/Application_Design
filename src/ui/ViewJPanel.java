    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = tblEmployee.getSelectedRow();
        if (selectedRowIndex<0){

            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;

        }

        txtName.setText(null);
        txtEmployeeId.setText(null);
        txtAge.setText(null);
        txtStartDate.setText(null);
        txtLevel.setText(null);
        txtGender.setText(null);
        txtTeamInfo.setText(null);
        txtPositionTitle.setText(null);
        txtPhone.setText(null);
        txtEmail.setText(null);
        
        txtBrowse.setIcon(null);
        
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        Employee selectedEmployee = (Employee) employeeList.get(selectedRowIndex);
        
        Employees.getInstance().getNameToEmployee().remove(selectedEmployee.getName());
        
        Employees.getInstance().getEmpIdToEmployee().remove(selectedEmployee.getEmpId());

        //history.deleteVitals(selectedVitals);

        JOptionPane.showMessageDialog(this, "Employee deleted");
        populateTable();

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        Employees employees = Employees.getInstance();
        
        Employee emp = null;
        
        if(!(txtName.getText() == null || txtName.getText().isEmpty()))
        {
            emp = employees.getNameToEmployee().get(txtName.getText());
        }
        
        if(!(txtEmployeeId.getText() == null || txtEmployeeId.getText().isEmpty()))
        {
            emp = employees.getEmpIdToEmployee().get(txtEmployeeId.getText());
        }
        
        if(null == emp)
        {
            JOptionPane.showMessageDialog(this, "Please Add Employee to the System");
            throw new UnsupportedOperationException("No Employee exist in the System");
        }
        populateForm(emp);
    }//GEN-LAST:event_btnSearchActionPerformed

