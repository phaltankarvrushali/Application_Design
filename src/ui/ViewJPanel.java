    private void populateTable() {
        
       
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        model.setRowCount(0);
        for (Employee vs : Employees.getInstance().getNameToEmployee().values()){
            
            Object[] row = new Object[12];
            employeeList.add(vs);
            row[0] = vs.getName();
            row[1] = vs.getEmpId();
            row[2] = vs.getAge();
            row[3] = vs.getGender();
            row[4] = DateTimeFormatter.ofPattern("MM-dd-yyyy").format(vs.getStartDate());
            row[5] = vs.getLevel();
            row[6] = vs.getTeamInfo();
            row[7] = vs.getPositionTitle();
            row[8] = vs.getContactInfo().getCell();
            row[9] = vs.getContactInfo().getEmail();
            row[10] = vs.getPhoto();
            
            model.addRow(row);
            
        }
            
    }
    
    private void populateForm(Employee selectedEmployee)
    {
        txtName.setText(String.valueOf( selectedEmployee.getName()));
        txtEmployeeId.setText(String.valueOf(selectedEmployee.getEmpId()));
        txtAge.setText(String.valueOf(selectedEmployee.getAge()));
        txtStartDate.setText(String.valueOf(selectedEmployee.getStartDate()));
        txtLevel.setText(String.valueOf(selectedEmployee.getLevel()));
        txtGender.setText(selectedEmployee.getGender());
        txtTeamInfo.setText(selectedEmployee.getTeamInfo());
        txtPositionTitle.setText(selectedEmployee.getPositionTitle());
        txtPhone.setText(String.valueOf(selectedEmployee.getContactInfo().getCell()));
        txtEmail.setText(selectedEmployee.getContactInfo().getEmail());
        
        ImageIcon ii = new ImageIcon(selectedEmployee.getPhoto());
        
        Image image = ii.getImage().getScaledInstance(txtBrowse.getWidth(), txtBrowse.getHeight(), Image.SCALE_SMOOTH);
        txtBrowse.setIcon(new ImageIcon(image));
    }
